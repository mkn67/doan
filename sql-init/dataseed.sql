UPDATE PHIEU_NHAP pn
SET pn.TONGTIEN = (
    SELECT SUM(SOLUONGNHAP * GIANHAP) FROM LO_HANG WHERE MAPN = pn.MAPN
);

-- Đồng bộ tồn kho sau khi bán
MERGE INTO LO_HANG lh
USING (
    SELECT MALO, SUM(SOLUONG) AS SOLD_QTY
    FROM CT_HOA_DON
    GROUP BY MALO
) sold ON (lh.MALO = sold.MALO)
WHEN MATCHED THEN UPDATE SET lh.SOLUONGTON = lh.SOLUONGNHAP - sold.SOLD_QTY;

-- Đồng bộ SOLUONGTON NULL thành SOLUONGNHAP
UPDATE LO_HANG SET SOLUONGTON = SOLUONGNHAP WHERE SOLUONGTON IS NULL;

