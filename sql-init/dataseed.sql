SET hd.TONGTIEN = (
    NVL((SELECT SUM(SOLUONG * DONGIA) FROM CT_HOA_DON WHERE MAHD = hd.MAHD), 0) +
    NVL((SELECT SUM(SOLUONG * DONGIA) FROM CT_HOA_DON_DV WHERE MAHD = hd.MAHD), 0)
);

