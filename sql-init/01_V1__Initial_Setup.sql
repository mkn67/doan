CREATE TABLE CT_HOA_DON_DV (
    MAHD    VARCHAR(10),
    MADV    VARCHAR(10),
    SOLUONG NUMERIC DEFAULT 1,
    DONGIA  NUMERIC(15,2),
    PRIMARY KEY (MAHD, MADV),
    FOREIGN KEY (MAHD) REFERENCES HOA_DON(MAHD),
    FOREIGN KEY (MADV) REFERENCES DICH_VU_KHAM(MADV)
);

CREATE OR REPLACE FUNCTION trg_ct_hoa_don_dv_after_func()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'INSERT' THEN
        UPDATE HOA_DON
        SET TONGTIEN = COALESCE(TONGTIEN, 0) + (NEW.SOLUONG * NEW.DONGIA)
        WHERE MAHD = NEW.MAHD;
    ELSIF TG_OP = 'UPDATE' THEN
        UPDATE HOA_DON
        SET TONGTIEN = COALESCE(TONGTIEN, 0)
                      - (OLD.SOLUONG * OLD.DONGIA)
                      + (NEW.SOLUONG * NEW.DONGIA)
        WHERE MAHD = NEW.MAHD;
    ELSIF TG_OP = 'DELETE' THEN
        UPDATE HOA_DON
        SET TONGTIEN = COALESCE(TONGTIEN, 0) - (OLD.SOLUONG * OLD.DONGIA)
        WHERE MAHD = OLD.MAHD;
    END IF;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER TRG_CT_HOA_DON_DV_AFTER
AFTER INSERT OR UPDATE OR DELETE ON CT_HOA_DON_DV
FOR EACH ROW EXECUTE FUNCTION trg_ct_hoa_don_dv_after_func();

