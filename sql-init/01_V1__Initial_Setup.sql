CREATE OR REPLACE VIEW V_CANH_BAO_TON_KHO AS
SELECT
    sp.MASP,
    sp.TENSP,
    sp.DONVITINH,
    COALESCE(SUM(lh.SOLUONGTON), 0) AS TONG_TON_HIEN_TAI,
    sp.TON_KHO_TOI_THIEU,
    CASE
        WHEN COALESCE(SUM(lh.SOLUONGTON), 0) = 0 THEN 'Het hang'
        WHEN COALESCE(SUM(lh.SOLUONGTON), 0) <= sp.TON_KHO_TOI_THIEU THEN 'Sap het'
        WHEN COALESCE(SUM(lh.SOLUONGTON), 0) <= sp.TON_KHO_TOI_THIEU * 2 THEN 'Canh bao'
        ELSE 'On dinh'
    END AS MUC_DO
FROM SAN_PHAM sp
LEFT JOIN LO_HANG lh ON sp.MASP = lh.MASP
                    AND lh.NGAYHETHAN > CURRENT_DATE
                    AND lh.SOLUONGTON > 0
GROUP BY sp.MASP, sp.TENSP, sp.DONVITINH, sp.TON_KHO_TOI_THIEU
HAVING COALESCE(SUM(lh.SOLUONGTON), 0) <= sp.TON_KHO_TOI_THIEU * 2
    OR COALESCE(SUM(lh.SOLUONGTON), 0) = 0
ORDER BY MUC_DO, TONG_TON_HIEN_TAI;


