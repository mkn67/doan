CREATE TABLE HANG_CHO (
    MAHC         VARCHAR(10) PRIMARY KEY,
    MAKH         VARCHAR(10),
    TEN_KHACH    VARCHAR(100),
    SO_THU_TU    NUMERIC NOT NULL,
    LOAI_KHACH   VARCHAR(20) DEFAULT 'Walk-in',
    MALH         VARCHAR(10) NULL,
    MANS_PHAN_CONG VARCHAR(10) NULL,
    TRANG_THAI   VARCHAR(30) DEFAULT 'Đang chờ',
    GIO_DANG_KY  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    GIO_VAO_KHAM TIMESTAMP NULL,
    GHI_CHU      VARCHAR(255),
    FOREIGN KEY (MAKH) REFERENCES KHACH_HANG(MAKH),
    FOREIGN KEY (MALH) REFERENCES LICH_HEN(MALH),
    FOREIGN KEY (MANS_PHAN_CONG) REFERENCES NHAN_SU(MANS)
);

CREATE SEQUENCE SEQ_HANG_CHO START 1 CACHE 1;

CREATE OR REPLACE FUNCTION trg_gen_mahc_func()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.MAHC IS NULL THEN
        NEW.MAHC := 'HC' || LPAD(nextval('SEQ_HANG_CHO')::text, 6, '0');
    END IF;
    IF NEW.SO_THU_TU IS NULL THEN
        SELECT COALESCE(MAX(SO_THU_TU), 0) + 1 INTO NEW.SO_THU_TU
        FROM HANG_CHO
        WHERE DATE(GIO_DANG_KY) = CURRENT_DATE;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER TRG_GEN_MAHC
BEFORE INSERT ON HANG_CHO
FOR EACH ROW EXECUTE FUNCTION trg_gen_mahc_func();

CREATE OR REPLACE VIEW V_HANG_CHO_HOM_NAY AS
SELECT
    hc.MAHC,
    hc.SO_THU_TU,
    hc.LOAI_KHACH,
    COALESCE(kh.HOTEN, hc.TEN_KHACH) AS TEN_KHACH,
    kh.SDT,
    hc.MANS_PHAN_CONG,
    ns.HOTEN AS TEN_BAC_SI,
    goi.TENGOI AS GOI_KHAM,
    hc.TRANG_THAI,
    hc.GIO_DANG_KY,
    hc.GIO_VAO_KHAM,
    ROUND(EXTRACT(EPOCH FROM (CURRENT_TIMESTAMP - hc.GIO_DANG_KY)) / 60) AS PHUT_CHO
FROM HANG_CHO hc
LEFT JOIN KHACH_HANG kh ON hc.MAKH = kh.MAKH
LEFT JOIN NHAN_SU ns ON hc.MANS_PHAN_CONG = ns.MANS
LEFT JOIN LICH_HEN lh ON hc.MALH = lh.MALH
LEFT JOIN GOI_KHAM goi ON lh.MAGOI = goi.MAGOI
WHERE DATE(hc.GIO_DANG_KY) = CURRENT_DATE
  AND hc.TRANG_THAI NOT IN ('Hoàn thành', 'Bỏ về')
ORDER BY
    CASE WHEN hc.LOAI_KHACH = 'Walk-in' THEN 0 ELSE 1 END,
    hc.SO_THU_TU;

CREATE INDEX IDX_HC_NGAY  ON HANG_CHO (GIO_DANG_KY);
CREATE INDEX IDX_HC_TRANG ON HANG_CHO (TRANG_THAI);
CREATE INDEX IDX_HC_LOAI  ON HANG_CHO (LOAI_KHACH);


